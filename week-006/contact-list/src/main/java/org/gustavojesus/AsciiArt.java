package org.gustavojesus;

class AsciiArt {
    /**
     * Prints a menu art in the console.
     *
     * This function displays a simple ASCII art representation of a menu. The art is a stylized representation
     * of a contact list application.
     *
     * @return This function does not return any value. It only prints the ASCII art to the console.
     */
    public static void printMenuArt() {
        System.out.println("\n" +
                "  ____   ___  _   _ _____ ___ _   _  ____ \n" +
                " / ___| / _ \\| | | |_   _|_ _| \\ | |/ ___|\n" +
                "| |     | | | | | | | | | | ||  \\| | |    \n" +
                "| |___ | |_| | |_| | | | | | || |\\  | |___ \n" +
                " \\____| \\___/ \\___/  |_||___|_| \\_|\\____|\n" +
                "===========================================\n");
    }
}